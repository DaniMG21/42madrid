#include <stdio.h>
#include <unistd.h>

void	ft_swap(int *a, int *b)
{	
	int aux;

	aux = *a;
	*a = *b;
	*b = aux;

	printf("%d\n", *a);
}

int		main()
{
	int n;
	int m;
	
	n = 5;
	m = 7;

	ft_swap(&n, &m);
}
