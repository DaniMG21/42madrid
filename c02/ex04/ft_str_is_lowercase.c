int		ft_str_is_lowercase(char *str)
{
	int x;
	int cont;

	x = 0;
	cont = 1;
	while (str[x] != '\0')
	{
		if (str[x] < 'a' || str[x] > 'z')
			cont = 0;
		x++;
	}
	return (cont);
}
